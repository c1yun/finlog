    write(content_path, json.dumps(items, ensure_ascii=False, indent=2) + "\n")

    display_titles = {slug: title.replace("\n", " ") for slug, (title, _) in CARD_COPY.items()}
    meta_path = ROOT / "cards_meta.json"
    meta = json.loads(meta_path.read_text(encoding="utf-8"))
    for item in meta:
        item["title"] = display_titles[item["slug"]]
    write(meta_path, json.dumps(meta, ensure_ascii=False, indent=2) + "\n")

    html_path = ROOT / "cards.html"
    html = html_path.read_text(encoding="utf-8")
    match = re.search(r"const SETS = (\[.*?\]);", html, flags=re.DOTALL)
    if not match:
        raise RuntimeError("Could not locate card metadata")
    sets = json.loads(match.group(1))
    for item in sets:
        item["title"] = display_titles[item["slug"]]
    serialized = json.dumps(sets, ensure_ascii=False)
    html = html[: match.start(1)] + serialized + html[match.end(1) :]
    write(html_path, html)


